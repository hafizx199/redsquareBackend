// Function to implement the FizzBuzz logic
function fizzBuzz(n) {
    for (let i = 1; i <= n; i++) {
        if (i % 3 === 0 && i % 5 === 0) {
            console.log("FizzBuzz");
        } else if (i % 3 === 0) {
            console.log("Fizz");
        } else if (i % 5 === 0) {
            console.log("Buzz");
        } else {
            console.log(i);
        }
    }
}

// Input the value of n in the command line 
const n = process.argv[2] || 15;


// Run the function with the value of n
fizzBuzz(Number(n));
